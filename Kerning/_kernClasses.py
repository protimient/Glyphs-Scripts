from collections import OrderedDict


class kern_classer:
    def __init__(self):
        self.glyph_kern_classes = OrderedDict()

    def __call__(self, gn, **kwargs):
        self.glyph_kern_classes[gn] = kwargs

    def __iter__(self):
        # yield from self.glyph_kern_classes.items()
        for x in self.glyph_kern_classes.items():
            yield x

    def get(self, x):
        return self.glyph_kern_classes.get(x)


